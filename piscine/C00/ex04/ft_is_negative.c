#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ch1;
	char	ch2;

	ch1 = 'N';
	ch2 = 'P';
	if (n < 0)
	{
		write(1, &ch1, 1);
	}
	else
	{
		write(1, &ch2, 1);
	}
}
